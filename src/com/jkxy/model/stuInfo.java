package com.jkxy.model;

public class stuInfo {
	private String glasses_version;
	private String glasses_name;
	private String glasses_made;
	private String glasses_price;
	private String glasses_mess;
	
	public String getGlasses_version() {
		return glasses_version;
	}
	public void setGlasses_version(String glasses_version) {
		this.glasses_version = glasses_version;
	}
	public String getGlasses_name() {
		return glasses_name;
	}
	public void setGlasses_name(String glasses_name) {
		this.glasses_name = glasses_name;
	}
	public String getGlasses_made() {
		return glasses_made;
	}
	public void setGlasses_made(String glasses_made) {
		this.glasses_made = glasses_made;
	}
	public String getGlasses_price() {
		return glasses_price;
	}
	public void setGlasses_price(String glasses_price) {
		this.glasses_price = glasses_price;
	}
	public String getGlasses_mess() {
		return glasses_mess;
	}
	public void setGlasses_mess(String glasses_mess) {
		this.glasses_mess = glasses_mess;
	}
	
}
